public class Ejercicio_If {

    public static void main(String[] args) {
        int numerolf = 0;
        //if
        if (numerolf < 0) {
            System.out.println("Negativo");
        } else if (numerolf > 0) {
            System.out.println("Positivo");
        } else {
            System.out.println("Es cero");
        }

    }
}
